/* ===== APPLY LANGUAGE ===== */
function applyLang(lang) {
  const dict = T[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = dict[el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = dict[el.dataset.i18nHtml];
    if (v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const v = dict[el.dataset.i18nPh];
    if (v !== undefined) el.placeholder = v;
  });

  const page = document.documentElement.dataset.page;
  if (page && dict['title.' + page]) document.title = dict['title.' + page];
  document.documentElement.lang = lang === 'en' ? 'en' : 'ru';

  const lbl = document.getElementById('headerLangLabel');
  if (lbl) lbl.textContent = lang.toUpperCase();
}

/* ===== HEADER SWITCHER ===== */
function initHeaderSwitcher() {
  const wrap = document.getElementById('headerLangSwitch');
  const btn  = document.getElementById('headerLangBtn');
  const drop = document.getElementById('headerLangDropdown');
  if (!wrap || !btn) return;

  btn.addEventListener('click', e => {
    e.stopPropagation();
    wrap.classList.toggle('open');
  });
  document.addEventListener('click', () => wrap.classList.remove('open'));

  drop.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', () => {
      const lang = opt.dataset.lang;
      wrap.classList.remove('open');
      localStorage.setItem('lang', lang);
      document.body.classList.add('lang-changing');
      setTimeout(() => {
        applyLang(lang);
        document.body.classList.remove('lang-changing');
      }, 190);
    });
  });
}

/* ===== LANGUAGE PICKER OVERLAY ===== */
function initLangPicker() {
  const overlay = document.getElementById('langOverlay');
  if (!overlay) return;

  const saved = localStorage.getItem('lang');
  if (saved) {
    overlay.remove();
    applyLang(saved);
    return;
  }

  /* show after short delay */
  setTimeout(() => overlay.classList.add('lang-overlay--visible'), 150);

  const selector = overlay.querySelector('.lang-selector');
  overlay.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', () => {
      const lang = opt.dataset.lang;
      localStorage.setItem('lang', lang);
      applyLang(lang);

      /* close dropdown */
      selector && selector.classList.remove('open');

      /* fade out overlay */
      overlay.classList.add('lang-overlay--hiding');

      /* show logo animation: remove is-hidden briefly then re-add */
      const pt = document.getElementById('pageTransition');
      if (pt) {
        pt.classList.remove('is-hidden');
        setTimeout(() => pt.classList.add('is-hidden'), 100);
      }

      setTimeout(() => overlay.remove(), 600);
    });
  });

  /* CSS :hover handles dropdown open/close;
     also support click for touch devices */
  selector && selector.addEventListener('click', e => {
    e.stopPropagation();
    selector.classList.toggle('open');
  });
  document.addEventListener('click', () => {
    selector && selector.classList.remove('open');
  });
}

/* ===== EXPOSE helper for script.js form handler ===== */
window.getI18n = key => {
  const lang = localStorage.getItem('lang') || 'ru';
  return (T[lang] && T[lang][key]) || key;
};

/* ===== BOOT ===== */
document.addEventListener('DOMContentLoaded', () => {
  initLangPicker();
  initHeaderSwitcher();
  const saved = localStorage.getItem('lang');
  if (saved) applyLang(saved);
});
